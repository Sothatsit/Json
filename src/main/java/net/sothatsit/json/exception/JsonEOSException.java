package net.sothatsit.json.exception;

public class JsonEOSException extends JsonException {

    public JsonEOSException() {
        super();
    }

    public JsonEOSException(String message) {
        super(message);
    }

    public JsonEOSException(String message, Throwable cause) {
        super(message, cause);
    }

    public JsonEOSException(Throwable cause) {
        super(cause);
    }

}
