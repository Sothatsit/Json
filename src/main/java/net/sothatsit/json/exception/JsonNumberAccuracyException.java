package net.sothatsit.json.exception;

public class JsonNumberAccuracyException extends JsonException {

    public JsonNumberAccuracyException() {
        super();
    }

    public JsonNumberAccuracyException(String message) {
        super(message);
    }

    public JsonNumberAccuracyException(String message, Throwable cause) {
        super(message, cause);
    }

    public JsonNumberAccuracyException(Throwable cause) {
        super(cause);
    }

}
